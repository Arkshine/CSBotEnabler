void PrecacheRadioSounds()
{
	ke::AString modName(GET_GAME_INFO(PLID, GINFO_NAME));

	char path[260];

	for (size_t i = 0; i < ARRAY_LENGTH(RadioSoundsList); ++i)
	{
		ke::SafeSprintf(path, sizeof(path), "%s/sound/radio/bot/%s", modName.chars(), RadioSoundsList[i]);

		if (ke::file::IsFile(path))
		{
			PRECACHE_GENERIC(STRING(ALLOC_STRING(&path[modName.length() + 1])));
		}
		else
		{
			LOG_CONSOLE(PLID, "The following sound doesn't exist: \"%s\"", &path[modName.length() + 1]);
		}
	}
}